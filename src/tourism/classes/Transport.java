package tourism.classes;

public class Transport extends Entity {
    private String name;
    private String number;

    public Transport() {
    }

    public Transport(String number, String name) {
        this.number = number;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}